/*
 * public: Highest visibility. Can be accessed from anywhere. Even from different packages.
 * private: Least visibility. Can only be accessed in the same class.
 */


public class PublicPrivateDifference {
	public static void main(String[] args) {
		System.out.println("public and private difference - ");
		System.out.println("public: Highest visibility. Can be accessed from anywhere. Even from different packages.");
		System.out.println("private: Least visibility. Can only be accessed in the same class.");
	}
}
